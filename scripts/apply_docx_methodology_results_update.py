def insert_block_before(paragraph, lines):
    for text, style in reversed(lines):
        new_p = paragraph.insert_paragraph_before(text)
        if style:
            new_p.style = style


def find_paragraph(doc, target_text):
    for p in doc.paragraphs:
        if p.text.strip() == target_text:
            return p
    raise ValueError(f'Could not find paragraph: {target_text}')


